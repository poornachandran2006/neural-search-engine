import asyncio
import json
import time
from datetime import datetime, timezone
from pathlib import Path

from groq import Groq
from app.core.config import settings
from app.core.logging import get_logger
from app.services.query.pipeline import run_query_pipeline

logger = get_logger(__name__)

_groq_client = Groq(api_key=settings.groq_api_key)

QUESTIONS_PATH = Path("/app/evaluation/dataset/questions.json")
RESULTS_PATH = Path("/app/evaluation/results/benchmark_results.json")

# Sequential execution with delays — Groq free tier is 30 RPM
_DELAY_BETWEEN_QUESTIONS = 8.0  # seconds between questions


# ─── Metric 1: Recall@5 ──────────────────────────────────────────────────────

def compute_recall_at_5(chunks: list[dict], keywords: list[str]) -> float:
    if not chunks or not keywords:
        return 0.0
    for chunk in chunks[:5]:
        text = chunk.get("text", chunk.get("text_preview", "")).lower()
        matches = sum(1 for kw in keywords if kw.lower() in text)
        if matches >= min(2, len(keywords)):
            return 1.0
    return 0.0


# ─── Metric 2: Faithfulness ──────────────────────────────────────────────────

def compute_faithfulness(context: str, answer: str) -> float:
    prompt = f"""You are an evaluation judge for RAG systems.

Context (the only information the system had access to):
{context[:3000]}

Answer generated by the system:
{answer[:1000]}

Task: Evaluate whether the answer contains ONLY information that is present in the context.
- Score 1.0: Every claim in the answer is directly supported by the context
- Score 0.5: Most claims are supported but there are minor unsupported additions
- Score 0.0: The answer contains significant information not present in the context

Respond with ONLY a JSON object: {{"score": <float between 0.0 and 1.0>, "reason": "<one sentence>"}}
No explanation outside the JSON."""

    try:
        raw = _groq_with_retry([{"role": "user", "content": prompt}], 128)
        if raw.startswith("```"):
            raw = raw.split("```")[1]
            if raw.startswith("json"):
                raw = raw[4:]
        return float(json.loads(raw.strip()).get("score", 0.5))
    except Exception as e:
        logger.error("faithfulness_judge_failed", error=str(e))
        return 0.5


# ─── Metric 3: Answer Relevancy ──────────────────────────────────────────────

def compute_answer_relevancy(question: str, answer: str) -> float:
    prompt = f"""You are an evaluation judge for RAG systems.

Question asked by the user:
{question}

Answer generated by the system:
{answer[:1000]}

Task: Evaluate how well the answer addresses the question.
- Score 1.0: The answer directly and completely addresses the question
- Score 0.5: The answer partially addresses the question or is vague
- Score 0.0: The answer does not address the question at all

Respond with ONLY a JSON object: {{"score": <float between 0.0 and 1.0>, "reason": "<one sentence>"}}
No explanation outside the JSON."""

    try:
        raw = _groq_with_retry([{"role": "user", "content": prompt}], 128)
        if raw.startswith("```"):
            raw = raw.split("```")[1]
            if raw.startswith("json"):
                raw = raw[4:]
        return float(json.loads(raw.strip()).get("score", 0.5))
    except Exception as e:
        logger.error("relevancy_judge_failed", error=str(e))
        return 0.5

def _groq_with_retry(messages: list, max_tokens: int = 128, retries: int = 4) -> str:
    """Call Groq with exponential backoff on rate limit errors."""
    for attempt in range(retries):
        try:
            resp = _groq_client.chat.completions.create(
                model=settings.groq_model,
                messages=messages,
                temperature=0.0,
                max_tokens=max_tokens,
                stream=False,
            )
            return resp.choices[0].message.content.strip()
        except Exception as e:
            if "rate_limit" in str(e).lower() and attempt < retries - 1:
                wait = 10 * (attempt + 1)
                logger.warning("groq_rate_limit_retry", attempt=attempt+1, wait=wait)
                time.sleep(wait)
            else:
                raise
    return ""

# ─── Single question evaluator ────────────────────────────────────────────────

async def _evaluate_one(q: dict, index: int, total: int) -> dict:
    """Evaluate a single question sequentially."""
    qid = q["id"]
    question = q["question"]
    keywords = q.get("relevant_chunk_keywords", [])

    logger.info("evaluating_question", id=qid, n=f"{index+1}/{total}")

    try:
        pipeline_result = await run_query_pipeline(question)
        chunks = pipeline_result["chunks"]

        context = "\n\n".join(
            c.get("text", c.get("text_preview", "")) for c in chunks
        )

        from app.services.rag.prompt_builder import build_single_doc_prompt
        messages = build_single_doc_prompt(question, chunks)

        answer = await asyncio.to_thread(
            _groq_with_retry, messages, 512
        )

        faithfulness, relevancy = await asyncio.gather(
            asyncio.to_thread(compute_faithfulness, context, answer),
            asyncio.to_thread(compute_answer_relevancy, question, answer),
        )

        recall = compute_recall_at_5(chunks, keywords)

        logger.info(
            "question_evaluated",
            id=qid,
            recall=recall,
            faithfulness=round(faithfulness, 3),
            relevancy=round(relevancy, 3),
        )

        return {
            "id": qid,
            "question": question,
            "answer": answer,
            "recall_at_5": recall,
            "faithfulness": round(faithfulness, 4),
            "answer_relevancy": round(relevancy, 4),
            "chunks_retrieved": len(chunks),
            "source_files": pipeline_result["source_files"],
            "intent": pipeline_result["intent"],
        }

    except Exception as e:
        logger.error("question_failed", id=qid, error=str(e))
        return {
            "id": qid,
            "question": question,
            "error": str(e),
            "recall_at_5": 0.0,
            "faithfulness": 0.0,
            "answer_relevancy": 0.0,
            "chunks_retrieved": 0,
            "source_files": [],
            "intent": "unknown",
        }


# ─── Main benchmark runner ────────────────────────────────────────────────────

async def run_benchmark() -> dict:
    if not QUESTIONS_PATH.exists():
        raise FileNotFoundError(f"Questions file not found: {QUESTIONS_PATH}")

    dataset = json.loads(QUESTIONS_PATH.read_text())
    questions = dataset["questions"]
    n = len(questions)

    logger.info("benchmark_started", total_questions=n)

    # Run questions sequentially with delay to respect Groq 30 RPM limit
    per_question_results = []
    for i, q in enumerate(questions):
        result = await _evaluate_one(q, i, n)
        per_question_results.append(result)
        if i < n - 1:
            logger.info("benchmark_cooldown", seconds=_DELAY_BETWEEN_QUESTIONS)
            await asyncio.sleep(_DELAY_BETWEEN_QUESTIONS)

    # Aggregate metrics
    recall_scores      = [r["recall_at_5"]      for r in per_question_results]
    faithfulness_scores = [r["faithfulness"]     for r in per_question_results]
    relevancy_scores   = [r["answer_relevancy"]  for r in per_question_results]

    results = {
        "run_at": datetime.now(timezone.utc).isoformat(),
        "total_questions": n,
        "documents": dataset.get("documents", []),
        "metrics": {
            "recall_at_5":      round(sum(recall_scores)       / n, 4),
            "faithfulness":     round(sum(faithfulness_scores)  / n, 4),
            "answer_relevancy": round(sum(relevancy_scores)     / n, 4),
        },
        "per_question": per_question_results,
    }

    # Write ONLY after full completion — never overwrite with partial data
    RESULTS_PATH.parent.mkdir(parents=True, exist_ok=True)
    RESULTS_PATH.write_text(json.dumps(results, indent=2))
    logger.info("benchmark_complete", metrics=results["metrics"])

    return results